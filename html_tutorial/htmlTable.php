   <h2>HTML Table - Adding a Border</h2>
   <p>If you do not specify a border for the table, it will be displayed without borders.<br><br>A border is set using the CSS <b>border</b> property:</p>
   <div class="w3-panel w3-card-2 w3-light-grey"> 
      <h4>Example</h4>
        <div class="w3-code htmlHigh notranslate">
        &lt;style&gt;<br>table, th, td {<br>border:1px solid black<br>}<br>&lt;/style&gt;
