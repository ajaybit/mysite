      <h2>Defining an HTML Table</h2><p>An HTML table is defined with the <strong><code>&lt;table&gt;</code></strong> tag.<br><br>Each table row is defined with the <strong><code>&lt;tr&gt;</code></strong> tag. A table header is defined with the <strong><code>&lt;th&gt;</code></strong> tag. By default, table headings are bold and centered. A table data/cell is defined with the <strong><code>&lt;td&gt;</code></strong> tag.</p>
    <div class="w3-panel w3-card-2 w3-light-grey"> 
